package lesson01;

public class CharType {
	public static void main(String[] args) {
		
		char a = 'A';
		System.out.println("a :"+ a);
		
		int b = a;
		System.out.println("b : "+ b);
		
		char c = 66;
		System.out.println("c : "+ c);
		
		int d = a+b;
		System.out.println("d: " + d);
	
		String 한글변수 = "한글이름";
		System.out.println("한글 이름 출력 :" + 한글변수);
	}
}
